public class Addition {
    public static void main(String[] args) {
        // Define two numbers
        int num1 = 5;
        int num2 = 3;

        // Perform addition
        int sum = num1 + num2;

        // Output the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
